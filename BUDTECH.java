import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner s = new Scanner(System.in);
		int t = s.nextInt();
		while(t-- > 0){
		    int x = 1000*(s.nextInt());
		    int sp = x/2;
		    int div = sp/5;
		    System.out.println(div);
		}
	}
}
